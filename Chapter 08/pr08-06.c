#include <stdio.h>

int is_leap(int year);

int main(void)
{
	int year;

	printf("연도를 입력하시오: ");
	scanf("%d", &year);
	
	if (is_leap(year) == 0)
		printf("%d년은 윤년입니다.", year);
	else
		printf("%d년은 윤년이 아닙니다.", year);

	return 0;
}

int is_leap(int year)
{
	if ((year % 4 == 0 && year % 100 != 0) || year % 400 == 0)
		return 0;
	else
		return 1;
}